import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
df=pd.read_csv('/content/sample_data/homeprices.csv')
#print(df) This Dataset only Contain Two Colums That is 1.Area and 2.Price


#USe Scatter plot to get idea how are the data is distributed
plt.xlabel("AREA(sqmt)")
plt.ylabel("PRICE($)")
plt.title("Actual Price")
plt.scatter(df.Area,df.Price,color="red",marker="+")
plt.show()

reg=LinearRegression()
reg.fit(df[['Area']],df.Price)

print("It has been Trained ")
a=int(input("Enter Area to Predict Price:"))
predicted_price = reg.predict([[a]]) # Store the predicted price
print("Predicted Price is:", predicted_price)


#USe Scatter plot to get idea how are the data is distributed
plt.xlabel("AREA(sqmt)")
plt.ylabel("PRICE($)")
plt.title("Predicted Price")
plt.scatter(a, predicted_price, color="red", marker="+")
plt.show()

